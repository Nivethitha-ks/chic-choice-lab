// Helper to get products by collection slug
export const getProductsByCollection = (collectionSlug: string): Product[] => {
  return products.filter(p => p.collection === collectionSlug);
};

// Helper to get products by category
export const getProductsByCategory = (category: string): Product[] => {
  return products.filter(p => p.category === category);
};

// Helper to get best sellers
export const getBestSellers = (): Product[] => {
  return products.filter(p => p.isBestSeller);
};

// Helper to get new arrivals
export const getNewArrivals = (): Product[] => {
  return products.filter(p => p.isNew);
};

// Helper to get trending products
export const getTrendingProducts = (): Product[] => {
  return products.filter(p => p.isTrending);
};

// Helper to get products under price
export const getProductsUnderPrice = (maxPrice: number): Product[] => {
  return products.filter(p => p.price <= maxPrice);
};

// Helper to get limited edition products
export const getLimitedEdition = (): Product[] => {
  return products.filter(p => p.isLimitedEdition);
};

